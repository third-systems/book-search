print('df made')

from langchain.text_splitter import RecursiveCharacterTextSplitter

text_splitter = RecursiveCharacterTextSplitter(
    chunk_size=500,
    chunk_overlap=100,
    length_function=len,
    is_separator_regex=False,
)

from sentence_transformers import SentenceTransformer

model = SentenceTransformer("Supabase/gte-small")
vectors = []
counter = 1

for index, row in df_sample.iterrows():
    splitted = text_splitter.create_documents([row['review']])
    for i, chunk in enumerate(splitted):
        text = chunk.page_content
        embeddings = model.encode(text)
        vectors.append({
            'item_id': row['item_id'],
            'chunk': text,
            'embedding': embeddings.tolist()
        })
    if counter % 100 == 0:
        print(f"Processed {counter:,} reviews")
    if counter % 1000 == 0:
        pd.DataFrame(vectors).head(counter).to_csv(
            "./out/reviews/reviews_" + str(counter) + ".csv", index=False
        )
    counter += 1

df_chunks = pd.DataFrame(vectors)
df_chunks.to_csv("./out/reviews/reviews_all.csv", index=False)